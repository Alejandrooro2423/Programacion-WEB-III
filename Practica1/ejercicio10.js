/*   ¿Cuando es conveniente utilizar un callback, y cuando es necesario utilizar una promesa? 
Callback: una función que pasas como parámetro para que se ejecute después (p. ej., tras 2s, al terminar una tarea, al hacer clic).
Promesa (Promise): un objeto que representa algo que terminará en el futuro (éxito o error). Permite encadenar (then/catch) o usar async/await.
es decir: callback para tareas muy simples y de una sola vez y promesa cuando la operación puede tarda.
*/